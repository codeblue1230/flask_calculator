class addition():
    def __init__(self, numOne, numTwo):
        self.num1 = numOne
        self.num2 = numTwo

    def add_the_numbers(self):
        return self.num1 + self.num2
    

class subtraction():
    def __init__(self, numOne, numTwo):
        self.num1 = numOne
        self.num2 = numTwo

    def subtract_the_numbers(self):
        return self.num1 - self.num2
    

class multiplication():
    def __init__(self, numOne, numTwo):
        self.num1 = numOne
        self.num2 = numTwo

    def multiply_the_numbers(self):
        return self.num1 * self.num2
    

class division():
    def __init__(self, numOne, numTwo):
        self.num1 = numOne
        self.num2 = numTwo

    def divide_the_numbers(self):
        return self.num1 / self.num2